package Animals;

public class Cow {
    private String naam;
    private double gewicht;
    private char geslacht;
    private int leeftijd;

    public Cow(String naam, double gewicht, char geslacht, int leeftijd) {
        this.naam = naam;
        this.gewicht = gewicht;
        this.geslacht = geslacht;
        this.leeftijd = leeftijd;
    }
}
